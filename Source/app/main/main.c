static void MAIN_Init(void);

/******************************************************************************/

/**
 * @brief  Initializes all elements
 * @param  None
 * @retval None
 */

static void MAIN_Init(void){
	/* Configure the system clock */
	SystemClock_Config();
	
	/* Reset of all peripherals, Initializes the Flash interface and the Systick. */
	HAL_Init();
	
	LED_Init();
}

/**
 * @brief  Main
 * @param  None
 * @retval None
 */

int main(void){
	/* Initialization */
	MAIN_Init();
	
	/* Main loop */
	while(1){
		LED_Control(LED_OFF);
		HAL_Delay(1000);
		LED_Control(LED_RED);
		HAL_Delay(1000);
	}
}








